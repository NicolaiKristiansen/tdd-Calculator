package TDD;

public class Calculator {

    static int add(int nrA, int nrB){
        int total;
        total = nrA + nrB;
        return total;
    }

    static  int subtract(int nrA, int nrB){
        int total = nrA - nrB;
        return total;
    }

    static int multiply(int nrA, int nrB){
        int total = nrA * nrB;
        return total;
    }

    static int divide(int nrA, int nrB){
        int total = nrA / nrB;
        return total;
    }
}
